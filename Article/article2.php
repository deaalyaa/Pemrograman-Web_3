					<h2>Indonesia mendidih, BMKG: Hindari keluar rumah pukul 10.00-16.00</h2>
					<p>JAKARTA: Badan Meteorologi, Klimatologi, dan Geofisika (BMKG) mengimbau masyarakat agar menghindari aktivitas di luar ruangan pada pukul 10.00–16.00 WIB selama periode cuaca panas ekstrem yang tengah melanda berbagai wilayah Indonesia</P>
					<p>Deputi Bidang Meteorologi BMKG, Guswanto, menjelaskan bahwa cuaca panas ekstrem diperkirakan masih akan berlangsung hingga akhir Oktober atau awal November 2025.</p>
					<p> “Fenomena cuaca ini disebabkan oleh kombinasi gerak semu matahari dan pengaruh monsun Australia,” ujar Guswanto.</p>
					<h4>CUACA PANAS EKSTREM DAN RISIKO KESEHATAN</h4>
					<p>Direktur Meteorologi Publik BMKG, Andri Ramdhani, menegaskan bahwa masyarakat perlu berhati-hati karena paparan sinar matahari pada jam-jam tertentu dapat menimbulkan risiko kesehatan serius.
					<p>“Warga diimbau untuk mengurangi aktivitas di luar ruangan pada pukul 10.00–16.00 WIB karena cuaca panas di sejumlah wilayah berpotensi membahayakan kesehatan,” kata Andri, dikutip dari Kompas.com, Sabtu (18/10).
					<p>Berdasarkan hasil pengamatan BMKG, indeks sinar ultraviolet (UV) di beberapa wilayah Indonesia kini berada pada level berisiko tinggi, terutama ketika paparan sinar matahari berlangsung lama. 
					<p>Dalam beberapa hari terakhir, BMKG mencatat suhu maksimum di siang hari mencapai 38 derajat Celsius, kondisi yang dapat memicu heatstroke atau kelelahan akibat panas berlebih.</p>
